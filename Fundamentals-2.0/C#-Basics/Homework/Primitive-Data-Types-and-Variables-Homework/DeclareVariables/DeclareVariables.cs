using System;

class DeclareVariables
{
    static void Main()
    {
        // 52130, -115, 4825932, 97, -10000

        ushort first = 52130;
        sbyte second = -115;
        int third = 4825932;
        sbyte fourth = 97;
        short fifth = -10000;

        Console.WriteLine("{0}, {1}, {2}, {3}, {4}", first, second, third, fourth, fifth);
    }
}

